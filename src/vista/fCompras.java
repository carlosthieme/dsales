        int fila = this.grDetalleArtiFact.getSelectedRow();

        DefaultTableModel datosFilaArtiFact = (DefaultTableModel) this.grDetalleArtiFact.getModel();
        DefaultTableModel datosFilaDetalleFact = (DefaultTableModel) this.grDetalleFact.getModel();
        
        Object[] object = new Object[5];
        object[0] = datosFilaArtiFact.getValueAt(fila, 0).toString();
        object[1] = datosFilaArtiFact.getValueAt(fila, 1).toString();
        object[2] = this.numCantidadArtFact.getValue().toString();
