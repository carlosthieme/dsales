    @SuppressWarnings("SuspiciousIndentAfterControlStatement")
    private void calcularTotal() {
        DefaultTableModel datosFilaDetalleFact = (DefaultTableModel) this.grDetalleFact.getModel();
        subTotal = 0;
        
        if(this.grDetalleFact.getRowCount()>0)
        for (int i = 0; i < this.grDetalleFact.getRowCount(); i++) {
            subTotal = subTotal + (Integer.parseInt(datosFilaDetalleFact.getValueAt(i, 3).toString()) * Integer.parseInt(datosFilaDetalleFact.getValueAt(i, 2).toString()));
        }

        this.txtSubTotal.setText(String.valueOf(subTotal));
        this.txtIVA.setText(String.valueOf(Math.round(subTotal * 0.19)));
        this.txtTotal.setText(String.valueOf(Math.round(subTotal * 1.19)));

        this.btnAgrArticuloFact.setEnabled(false);
        this.btnGrabFactura.setEnabled(true);
    }
    
    public static void iniciar() throws SQLException {
        fCompras fc = new fCompras();
        fc.setVisible(true);
        fc.setLocationRelativeTo(null);
        ControladorOrdenesCompra c = new ControladorOrdenesCompra(fc);
        ControladorCompras cc = new ControladorCompras(fc);
        fc.setCursor(new Cursor(Cursor.WAIT_CURSOR));
        c.listarCbProveedores(fc.cbProveOrden);
        c.listarCbEstado(fc.cbBusqEstadoOrden);
        c.listarOrdenes();
        fc.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
    }
    
    
